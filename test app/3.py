import turtle

ncolor="#004200"
polecolor="#302525"
turtle.Screen().setup(1600, 900)
turtle.Screen().bgcolor("#2b75ff")
turtle.Screen().title("flowers in the field")
def ground():
    ground= turtle.Turtle()
    ground.speed(10)
    ground.penup()
    ground.goto(-800, -400)
    ground.pendown()
    ground.hideturtle()
    ground.color("#1a1313")
    ground.begin_fill()
    ground.forward(1800)
    ground.left(-90)
    ground.forward(100)
    ground.left(-90)
    ground.forward(1800)
    ground.left(-90)
    ground.forward(100)
    ground.end_fill()
def mill1():
    n1 = turtle.Turtle()
    n1.speed(10)
    n1.hideturtle()
    n1.width(2)
    n1.color(ncolor)
    pole = turtle.Turtle()
    pole.hideturtle()
    pole.speed(10)
    pole.width(2)
    n1.speed(10)
    n1.hideturtle()
    px1=100
    pole.color(polecolor)
    pole.goto(0, -400)

    for i in range(25):
        n1.pendown()
        n1.forward(px1)
        n1.left(85)
        n1.forward(px1)
        n1.goto(0, 0)
    n1.hideturtle()
